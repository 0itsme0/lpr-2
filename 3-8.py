spisok = ['USA', 'Ukraine', 'Afrika', 'Kazahstan', 'Afganistan'] # Задаем переменную со списком стран
print(spisok) # Выводим этот список так, как видит Пайтон
print(sorted(spisok)) # Временно сортируем этот список в алфавитном порядке
print(spisok) # Показываем, что список никак не поменялся
spisok.reverse() # Перемешиваем наш список
print(spisok) # Смотрим, что список изменился
spisok.reverse() # Снова перемешиваем список
print(spisok) # Смотрим, что список изменился
spisok.sort() # Сортирую на постоянную основу
print(spisok) # Выводим сортированный список
spisok.reverse() # Выводим список в обратном порядке
print(spisok) # Смотрим на список
